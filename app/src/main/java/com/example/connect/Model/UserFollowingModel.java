package com.example.connect.Model;

public class UserFollowingModel {
    private String followingName, followingUid;

    public UserFollowingModel() {
    }

    public String getFollowingName() {
        return followingName;
    }

    public void setFollowingName(String followingName) {
        this.followingName = followingName;
    }

    public String getFollowingUid() {
        return followingUid;
    }

    public void setFollowingUid(String followingUid) {
        this.followingUid = followingUid;
    }

}
